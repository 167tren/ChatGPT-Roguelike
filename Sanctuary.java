public final class Sanctuary {
    private final int tileX;
    private final int tileY;

    public Sanctuary(int tileX, int tileY) {
        this.tileX = tileX;
        this.tileY = tileY;
    }

    public int getTileX() {
        return tileX;
    }

    public int getTileY() {
        return tileY;
    }
}
